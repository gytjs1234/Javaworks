package com.callor.var;

public class Var_01 {
	
	public static void main(String[] args) {
		int num1 = 55;
		int num2 = 33;

		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);

		int student = 55;
		int class1 = 33;

/**같은 변수명은 한블럭 내부에서 두번이상 선언할수없다
	**/	
		
		System.out.println(student + class1);
		System.out.println(student - class1);
		System.out.println(student * class1);
		System.out.println(student / class1);

//		변수 선언과 0으로 초기화(clear라고 표현)
		int intNum3=0;
		int intNum4=0;

		intNum3 = 55;
		intNum4 = 33;
		
		
		System.out.println(intNum3 +intNum4);
		System.out.println(intNum3 -intNum4);
		System.out.println(intNum3 *intNum4);
		System.out.println(intNum3 /intNum4);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}
}
